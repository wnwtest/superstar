<node TEXT="Frame Control域" ID="ID_1582425604" CREATED="1715160202972" MODIFIED="1715161635056"><richcontent TYPE="DETAILS">
<html>
  <head>
    
  </head>
  <body>
    <p>
      Frame Control域共2字节16位
    </p>
    <p>
