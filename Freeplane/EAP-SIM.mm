  <body>
    <p>
      EAP-SIM (Extensible Authentication Protocol-Subscriber Identity Module) 是一种基于无线网络的身份验证和连接协议。它主要使用在GSM网络的SIM卡进行身份验证。这种协议最初被设计出来应用于无线局域网 (WLAN) 的安全，但现在已经被广泛应用于其他类型的网络连接，如3G和4G网络。
    </p>
  </body>
</html>
</richcontent>
