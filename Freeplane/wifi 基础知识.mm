    </p>
    <p>
      外,如果地址全为1,例如FF-FF-FF-FF-FF-FF,则为MAC广播地址。
    </p>
    <p>
      ·第47位表示该MAC地址是全球唯一的还是本地唯一。故该位也称为G/L位
    </p>
