<html>
  <head>
    
  </head>
  <body>
    <p class="p">
      Low resolution motion estimation (LRME) finds the motion vector (MV) field between two low-resolution (downscaled) images. LRME should be tuned after IFE tuning. When TF is configured to use LRME, LRME should be tuned first.
    </p>
    <div class="p">
      For registration tuning, capture video with global motion that includes:

      <ul class="ul">
        <li class="li">
          A lab scene (or something similar) with sufficient details
        </li>
        <li class="li">
          Mimicry of slight hand-shake motion
        </li>
        <li class="li">
          Some moving objects (e.g., trains, rotating tables, a person moving)
        </li>
        <li class="li">
          Flat regions suitable for noise level estimation (ensure the flat region is at least 5% x 5% FOV)
        </li>
      </ul>
    </div>
    <div class="p">
      (Advanced) For transform confidence tuning, capture video with insufficient details that includes:

      <ul class="ul">
        <li class="li">
          A single object on a uniform background
        </li>
        <li class="li">
          Many occlusions and/or aperture problems (e.g., repetitive patterns such as carpets)
        </li>
      </ul>
    </div>
  </body>
